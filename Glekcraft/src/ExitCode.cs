namespace Glekcraft;

/// <summary>
/// An enumeration of exit codes that can be returned by the program.
/// </summary>
public enum ExitCode : int {
    /// <summary>
    /// The program exited successfully.
    /// </summary>
    Success = 0,

    /// <summary>
    /// The program encountered an error.
    /// </summary>
    Error = 1
}
