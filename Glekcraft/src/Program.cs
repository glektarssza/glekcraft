namespace Glekcraft;

/// <summary>
/// The class containing the program entry point.
/// </summary>
public class Program {
    /// <summary>
    /// The program entry point.
    /// </summary>
    /// <returns>
    /// A status code for the operating system.
    /// </returns>
    public static int Main() {
        Console.WriteLine("Hello, World!");
        return (int)ExitCode.Ok;
    }
}
